//What is JavaScript?

// JavaScript is a high-level, interpreted programming language used to create dynamic and interactive content on web pages.
// JavaScript is a powerful, lightweight, high-level scripting language used to make web pages interactive and dynamic.
// It’s one of the three core technologies of the web, along with HTML and CSS.
// If HTML is the structure (like bones) and CSS is the style (like clothes), then JavaScript is the brain – making things respond, react, and move.

// In simpler terms:
//      1. It makes web pages do things instead of just sitting there.
//      2. It is the third core technology of the Web:

//      HTML: structure/content
//      CSS: style/layout
//      JavaScript: behavior/interactivity


//  2. Server-side JavaScript
//      Runs on a server with platforms like Node.js.
//      Example: Powering APIs or databases.


// JavaScript in the Real World
//      You’ve seen JavaScript in action every day, even if you didn’t know it:
//      When you click a button and something happens without reloading the page – that's JavaScript.
//      When a website asks you to confirm your email is valid – that’s JavaScript.
//      When a slideshow or carousel automatically moves – that’s JavaScript.
//      When you type in a search box and see suggestions appear – again, JavaScript.


